# Quais são ?

# Adição: +

# Subtração : -

# Multiplicação : *

# Divisão : /

# Resto (Módulo) : %

# Expoente : **


num1 = 10

num2 = 25

num3 = 40

num4 = 75


# Soma entre dois números

puts (num1 + num2)

puts " "

# Subtração

puts (num3 - num2)

puts " "

# Multiplicação

puts (num4 * num1)

puts " "

# Divisão

puts (num4 / num3)

puts " "

# Resto

puts (num4 % num3)

puts " "

# Expoente 

puts (num2 ** num2)