# Métodos de Chama uma String


r = "eu sou uma String"

a = "Eu sou um dev:"

b = "Ruby"

idade = 22 


# Em letra maiúscula
puts(r.upcase)

puts " "

# Em letra minúscula
puts(r.downcase)

puts " "

# Escrevendo ela ao contrário
puts(r.reverse)

puts " "

# Escrevendo só a primeira letra em maiúscula
puts(r.capitalize)

puts " "

# Juntando duas string
puts(a + b)

puts " "

# Invertendo essas duas strings
puts(a << b)

puts " "

# Cocatenando duas Strings

puts("Estude ruby".concat ("mais"))

puts " "

# Interpolação

puts("Vagner tem #{idade} anos!")

# O ( #{} ) também cria a variável sem ter a necessidade de ter criado a mesma

